package com.tstar.model.custom;


public class ProjectDevice {
	private String projectId;
	private String rateId;
	private String projectName;
	private String price;
	private String deviceName;
	private String deviceModel;
	private String prepaymentAmount;
	private String singlePhone;	//單手機
	
	public String getRateId() {
		return rateId;
	}
	public void setRateId(String rateId) {
		this.rateId = rateId;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	public String getDeviceModel() {
		return deviceModel;
	}
	public void setDeviceModel(String deviceModel) {
		this.deviceModel = deviceModel;
	}
	public String getPrepaymentAmount() {
		return prepaymentAmount;
	}
	public void setPrepaymentAmount(String prepaymentAmount) {
		this.prepaymentAmount = prepaymentAmount;
	}
	public String getSinglePhone() {
		return singlePhone;
	}
	public void setSinglePhone(String singlePhone) {
		this.singlePhone = singlePhone;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	
	
	
}